package SX;

public class Mon {
    private String ma,tenM,ht;

    public Mon(String ma, String tenM, String ht) {
        this.ma = ma;
        this.tenM = tenM;
        this.ht = ht;
    }

    public String getMa() {
        return ma;
    }

    public String getTenM() {
        return tenM;
    }

    public String getHt() {
        return ht;
    }
}
