package J07027;

public class SV {
    private String maSv,name,sdt;

    public SV(String maSv, String name, String sdt) {
        this.maSv = maSv;
        this.name = name;
        this.sdt = sdt;
    }

    public String getMaSv() {
        return maSv;
    }

    public String getName() {
        return name;
    }

    public String getSdt() {
        return sdt;
    }
}
