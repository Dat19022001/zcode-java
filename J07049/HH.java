package J07049;

public class HH {
    private String maSP,ten;
    private int gia,thoihan;

    public HH(String maSP, String ten, int gia, int thoihan) {
        this.maSP = maSP;
        this.ten = ten;
        this.gia = gia;
        this.thoihan = thoihan;
    }

    public String getMaSP() {
        return maSP;
    }

    public String getTen() {
        return ten;
    }

    public int getGia() {
        return gia;
    }

    public int getThoihan() {
        return thoihan;
    }
}
