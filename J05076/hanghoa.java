package J05076;

public class hanghoa {
    private String ma,name,xeploai;

    public hanghoa(String ma, String name, String xeploai) {
        this.ma = ma;
        this.name = name;
        this.xeploai = xeploai;
    }

    public String getMa() {
        return ma;
    }

    public String getName() {
        return name;
    }

    public String getXeploai() {
        return xeploai;
    }
}
