package J05074;

public class SinhVien {
    private String maSV,name,lop;

    public SinhVien(String maSV, String name, String lop) {
        this.maSV = maSV;
        this.name = name;
        this.lop = lop;
    }

    public String getMaSV() {
        return maSV;
    }

    public String getName() {
        return name;
    }

    public String getLop() {
        return lop;
    }
}
