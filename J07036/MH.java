package J07036;

public class MH {
    private String ma,mon;
    private int tinchi;

    public MH(String ma, String mon, int tinchi) {
        this.ma = ma;
        this.mon = mon;
        this.tinchi = tinchi;
    }

    public String getMa() {
        return ma;
    }

    public String getMon() {
        return mon;
    }

    public int getTinchi() {
        return tinchi;
    }
}
