package J05069;

public class CLB {
    private String ma,ten;
    private int sl;

    public CLB(String ma, String ten, int sl) {
        this.ma = ma;
        this.ten = ten;
        this.sl = sl;
    }

    public String getMa() {
        return ma;
    }

    public String getTen() {
        return ten;
    }

    public int getSl() {
        return sl;
    }
}
